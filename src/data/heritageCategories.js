export const SQL_HERITAGE_CATEGORY_OPTIONS = SQL_HERITAGE_CATEGORY_SLUGS
  .map((slug) => HERITAGE_CATEGORY_OPTIONS.find((category) => category.value === slug))
  .filter(Boolean);

export const getHeritageCategory = (value) =>
  HERITAGE_CATEGORY_OPTIONS.find((category) => category.value === value);

export const formatHeritageCategoryLabel = (value, t) => {
  const category = getHeritageCategory(value);
  if (!category) return value || '';
  return t ? t(category.labelKey) : category.name;
};

export const getHeritageCategoryListBadge = (value) =>
  getHeritageCategory(value)?.listBadge || 'bg-gray-600 text-white';

export const getHeritageCategoryDetailBadge = (value) =>
  getHeritageCategory(value)?.detailBadge || 'bg-gray-100 text-gray-700 border-gray-300 dark:bg-gray-700 dark:text-gray-300 dark:border-gray-600';

export const getHeritageCategoryIcon = (value) =>
  getHeritageCategory(value)?.icon || 'landmark';

export const sortHeritageCategoryValues = (values) => {
  const order = new Map(HERITAGE_CATEGORY_OPTIONS.map((category, index) => [category.value, index]));
  return [...values].sort((a, b) => {
    const aOrder = order.has(a) ? order.get(a) : Number.MAX_SAFE_INTEGER;
    const bOrder = order.has(b) ? order.get(b) : Number.MAX_SAFE_INTEGER;
    if (aOrder !== bOrder) return aOrder - bOrder;
    return String(a).localeCompare(String(b), 'vi');
  });
};
